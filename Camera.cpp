#include "Camera.h"

//--------------------------------------------------------------
Camera::Camera(int _ID) {
    ID = _ID;

    gui.setup("CAMERA "+ofToString(ID)+" SETUP", "", ofGetWidth()-500, (70*ID)+10);
    //gui.add(enabled.set("ENABLED", false));
    gui.add(doPreview.set("PREVIEW", false));

    preview.allocate(ofGetWidth(),ofGetHeight());

}

//--------------------------------------------------------------
void Camera::begin() {
    ofEnableDepthTest();
    camera.begin();
}

//--------------------------------------------------------------
void Camera::end() {
    camera.end();
    ofDisableDepthTest();
}

//--------------------------------------------------------------
void Camera::drawGui() {
    if(enabledGui) {
        gui.draw();
        preview.draw(gui.getPosition().x, gui.getPosition().y+5+gui.getHeight(), 200,200);
    }
}

//--------------------------------------------------------------
void Camera::drawEditor() {
    camera.draw();
}

//--------------------------------------------------------------
void Camera::drawPlayer() {
}

